      real FUNCTION AMAS(IK)
C  DETERMINATION OF PARTICLE MASS
      IMPLICIT REAL*8 (A-H,O-Z)
c      REAL   AMAS
      COMMON /DATA1/CMIX(6,2),PMASM(18),PMASB(18),PGAMM(18),
     *PGAMB(18),MESO(9,2)
      IF(IK.EQ.55)GO TO 3
      IF(IK-36 < 0) THEN
      GOTO 1
      ELSE IF (IK-36  == 0) THEN
      GOTO 1
      ELSE IF (IK-36  > 0) THEN
      GOTO 2
      END IF
 1    AMAS=PMASM(IK)
      RETURN
 2    AMAS=PMASB(IK-36)
      RETURN
 3    AMAS=0.

      RETURN
      END


