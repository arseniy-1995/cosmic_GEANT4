        FUNCTION AMREZ(NUM)
        COMMON/OMGM/B1(37),B2(37)
        COMMON/ROM/D1(37),D2(37)
        REAL*8  AM

        real vran(1)

        call ranmar(vran,1)
        RE=vran(1)
        IF(NUM.EQ.17) GO TO 1
        IF(NUM.EQ.55) GO TO 3
        IF(NUM.EQ.10.OR.NUM.EQ.11.OR.NUM.EQ.16) GO TO 2
        AM=AMAS(NUM)
        AMREZ=AM
        RETURN
