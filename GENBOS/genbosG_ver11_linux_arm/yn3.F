   1    AMREZ=ALI(RE,B1,B2,37)
        RETURN
C     MASSA RO
   2    AMREZ=ALI(RE,D1,D2,37)
        RETURN
C     GAMMA
   3    AMREZ=0.00
        RETURN
        END


